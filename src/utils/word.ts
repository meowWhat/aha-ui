export function getFirstLetter(str: string) {
  if (typeof str !== 'string') {
    str = String(str)
  }
  str = str.trim()
  let uni = str.charCodeAt(0)
  if (uni > 40869 || uni < 19968) {
    return str.charAt(0)
  }
  return WORD.charAt(uni - 19968)
}

